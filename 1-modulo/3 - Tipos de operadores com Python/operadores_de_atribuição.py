##atribuição simples
saldo = 500
print (saldo)

##atribuição com adição
saldo = 500
saldo += 200  # saldo = saldo + 200
print(saldo)

##atribuição com subtração
saldo = 500
saldo -= 200  # saldo = saldo - 200
print(saldo)

##atribuição com multiplicação
saldo = 500
saldo *= 200  # saldo = saldo * 200
print(saldo)

##atribuição com divisão
saldo = 500
saldo /= 200  # saldo = saldo / 200
print(saldo)

##atribuição com divisão inteira
saldo = 500
saldo //= 200  # saldo = saldo / 200
print(saldo)

##atribuição com divisão resto
saldo = 500
saldo %= 200  # saldo = saldo % 200
print(saldo)

